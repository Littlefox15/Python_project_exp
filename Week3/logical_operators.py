#logical operators: AND, OR, NOT

#AND
print(4 > 3 and 2 < 3)
print(4 < 3 and 2 < 3)

#OR
print(4 > 3 or 2 < 3)
print(4 < 3 or 2 < 3)

#NOT
print(not 4 > 3)
print(not not 4 > 3)
print(not True)
print(not False)
print(not not True)

print(4 is 4)
print(2 is not 4)
print(2 is not 2)
print('land' in 'Finland')
print('F' in 'Sweden')
print('Lord' in 'Lord of the rings')